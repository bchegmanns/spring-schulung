package de.ithegmanns.spring.basic.el.b;

public class Configuration {

	private String veranstalter;
	
	private int anzahlBodyguards;
	
	// Getters and Setters:
	
	public String getVeranstalter() {
		return veranstalter;
	}

	public void setVeranstalter(String veranstalter) {
		this.veranstalter = veranstalter;
	}

	public int getAnzahlBodyguards() {
		return anzahlBodyguards;
	}

	public void setAnzahlBodyguards(int anzahlBodyguards) {
		this.anzahlBodyguards = anzahlBodyguards;
	}
	
}
